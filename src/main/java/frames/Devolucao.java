    private void btRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRegistrarActionPerformed
        String codigo;
        LocalDate dataD, dataA;
        String livro;
        String aluno;
        String professor;
        final DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        Emprestado e = new Emprestado();
        ProfessorDAO pd = new ProfessorDAO();
        AlunoDAO ad = new AlunoDAO();
        
        //Se nenhum radio button foi selecionado não permite alterações
        if(this.rbAluno.isSelected() == false && this.rbProfessor.isSelected() == false){
            JOptionPane.showMessageDialog(this, "Você deve preencher todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
        }else{
            //Leitura dos campos
            codigo = this.tfCodigo.getText();
            dataD = LocalDate.parse(this.tfDataD.getText(), dtf);
            dataA = LocalDate.parse(this.tfDataA.getText(), dtf);
            
            //Se for de tipo Professor, cadastra como o tipo
            if(this.rbProfessor.isSelected()){
                professor = this.cbProfessor.getSelectedItem().toString();
                livro = this.cbLivro.getSelectedItem().toString();
                long multa = 0;
                
                //Verifica se campos estão preenchidos
                if(codigo.equals("") || dataD.equals("") || dataA.equals("")){
                    JOptionPane.showMessageDialog(this, "Você deve preencher todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
                }else{
                    //Cadastro da devolucao
                    Livro l = new Livro();    
                    
                   // e.multar(professor, "Professor", dataA);
                    
                    l.realizarDevolução(Integer.parseInt(codigo), professor, livro, dataD, dataA);
                 
                    JOptionPane.showMessageDialog(this, "Devolução cadastrada com sucesso!");
                    this.limparCampos();
                }
            }
            //Se for tipo Aluno, cadastra como o tipo
            if(this.rbAluno.isSelected()){
                aluno = this.cbAluno.getSelectedItem().toString();
                livro = this.cbLivro.getSelectedItem().toString();
                long multa = 0;
                //Verifica os campos
                if(codigo.equals("") || dataD.equals("") || dataA.equals("")){
                    JOptionPane.showMessageDialog(this, "Você deve preencher todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
                }else{
                    //Cadastra a devolução
                    Livro l = new Livro();  
                    
                    //e.multar(aluno, "Aluno", dataA);
            
                    l.realizarDevolução(Integer.parseInt(codigo), aluno, livro, dataD, dataA);
                    
                    if(multa==0){
                        JOptionPane.showMessageDialog(this, "Devolução cadastrada com sucesso!\n Não há multa!");
                        this.limparCampos();
                    }else{
                        JOptionPane.showMessageDialog(this, "Devolução cadastrada com sucesso!\n Multa: "+multa);
                        this.limparCampos();
                    }
                }
            }
        }
    }//GEN-LAST:event_btRegistrarActionPerformed

