    /**
     * Preenche o combo box com Alunos
     */
    public void preencheListaA(){
        EmprestadoDAO ea = new EmprestadoDAO();
        ArrayList<Emprestado> emprestado_alunos = new ArrayList<>();
        emprestado_alunos = ea.buscarEmprestadoA();
        for(int i=0; i<emprestado_alunos.size(); i++){
            this.cbAluno.addItem(Integer.toString(emprestado_alunos.get(i).getAluno()));
        }
    }
    
    /**
     * Preenche o combo box com Professores
     */
    public void preencheListaP(){
        EmprestadoDAO ep = new EmprestadoDAO();
        ArrayList<Emprestado> emprestado_professores = new ArrayList<>();
        emprestado_professores = ep.buscarEmprestadoP();
        for(int i=0; i<emprestado_professores.size(); i++){
            this.cbAluno.addItem(Integer.toString(emprestado_professores.get(i).getProfessor()));
            System.out.println("Nome: "+emprestado_professores.get(i).getProfessor());
        }
    }
    
    /**
     * Preenche o combo box com Livros
     */
    public void preencheLivros(){
        LivroDAO l = new LivroDAO();
        ArrayList<Livro> livros = new ArrayList<>();
        livros = l.buscarTodos();
        for(int i=0; i<livros.size(); i++){
            this.cbLivro.addItem(livros.get(i).getTítulo());
        }
    }    

